public class Main {

  public static void main(String[] args) {
    for (char i = 'a'; i <= 'z'; i++) {
      int letterCode;
      letterCode = i;
      System.out.println(i + " - " + letterCode);
    }
    for (char x = 'A'; x <= 'Z'; x++) {
        int bigLetterCode;
        bigLetterCode = x;
        System.out.println(x + " - " + bigLetterCode);
    }
  }
}
